    private void table_dondhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_dondhMouseClicked
        try {
            int index = table_dondh.getSelectedRow();
            tf_sohd.setText(table_dondh.getValueAt(index, 0).toString());
            SimpleDateFormat formatter = new SimpleDateFormat("dd/mm/yyyy");
            Date date = new SimpleDateFormat("yyyy-mm-dd").parse(table_dondh.getValueAt(index, 1).toString());
            String strDate = formatter.format(date);
            tf_ngaydh.setText(strDate);
            jcb_tenncc.setSelectedItem(table_dondh.getValueAt(index, 2));
        } catch (Exception e) {
        }
    }//GEN-LAST:event_table_dondhMouseClicked

