    public void hienthi() {
        DefaultTableModel tbn = (DefaultTableModel) table_dondh.getModel();
        try {
            String query = "select DONDH.SoDh,DONDH.NgayDh,NHACC.TenNhaCc from DONDH inner join NHACC on DONDH.MaNhaCc=NHACC.MaNhaCc order by DONDH.SoDh asc";
            Connection conn = DAO.getConnection();
            PreparedStatement ps = conn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector vt = new Vector();
                vt.add(rs.getString("DONDH.SoDh"));
                vt.add(rs.getString("DONDH.NgayDh"));
                vt.add(rs.getString("NHACC.TenNhaCc"));
                tbn.addRow(vt);
            }
            table_dondh.setModel(tbn);
        } catch (Exception e) {
            System.out.println(e);
        }
    }

