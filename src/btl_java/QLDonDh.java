    public void Add_DONDH() {
        DefaultTableModel tbn = (DefaultTableModel) table_dondh.getModel();
        try {
            String query = "insert into DONDH values(?,?,?)";
            Connection conn = DAO.getConnection();
            PreparedStatement ps = conn.prepareStatement(query);
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-mm-dd");
            Date date = new SimpleDateFormat("dd/mm/yyyy").parse(tf_ngaydh.getText());
            String strDate = formatter.format(date);
            ps.setString(2, strDate);
            ps.setString(3, getMaNhaCc(jcb_tenncc.getSelectedItem().toString()));
            ps.setString(1, tf_sohd.getText());
            int check = ps.executeUpdate();
            if (check > 0) {
                tf_sohd.setText("");
                tf_ngaydh.setText("");
                tbn.setRowCount(0);
                hienthi();
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Không thêm được!");
            }
        } catch (Exception e) {
        }
    }

